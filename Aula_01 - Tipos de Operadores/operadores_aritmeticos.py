produto_1 = 10
produto_2 = 20

print(produto_1 + produto_2) # Soma
print(produto_1 - produto_2) # Subtração
print(produto_1 * produto_2) # Multiplicação
print(produto_1 / produto_2) # Divisão
print(produto_1 % produto_2) # Módulo (Resto da divisão)
print(produto_1 ** produto_2) # Potenciação


x = (10 + 5) * 4 # Primeiro é executado o parentêses
y = 10 / 2 + 25 * 2 - 100 ** 2 # Primeiro é executado a potenciação,
                               # se a ordem de precedência é a mesma é 
                               # executado o que está a esquerda primeiro
print(x)
print(y)